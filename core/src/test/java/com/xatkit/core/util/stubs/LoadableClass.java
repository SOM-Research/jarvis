package com.xatkit.core.util.stubs;

/**
 * A {@link Class} that can be constructed with two {@link String} parameters.
 * <p>
 * This class is used to test the Xatkit {@link com.xatkit.util.Loader}.
 */
public class LoadableClass {

    private String s1;
    private String s2;

    public LoadableClass(String s1, String s2) {
        this.s1 = s1;
        this.s2 = s2;
    }
}
